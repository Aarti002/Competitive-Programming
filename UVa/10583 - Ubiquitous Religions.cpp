  int n,m,i,j,cases;
  cases = 1;
  while(scanf("%d %d",&n,&m) && n !=0 && m != 0) {
  	UFDS UF(n);
  	while(m--) {
  		scanf("%d %d",&i,&j);
  		UF.unionSet(i,j);
  	}
  	printf("Case %d: %d\n",cases++, UF.numDisjointSets());
  }
 return 0;

